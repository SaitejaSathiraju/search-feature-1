<script>
  document.addEventListener('DOMContentLoaded', () => {
    const scrollWrapper = document.querySelector('.horizontal-scroll-wrapper');
    const leftBtn = document.querySelector('.left-scroll');
    const rightBtn = document.querySelector('.right-scroll');
    const autoScrollToggle = document.querySelector('.auto-scroll-toggle');
    let autoScrollInterval;
    let isAutoScrolling = true;

    function scrollLeft() {
      scrollWrapper.scrollBy({
        left: -300,
        behavior: 'smooth'
      });
    }

    function scrollRight() {
      scrollWrapper.scrollBy({
        left: 300,
        behavior: 'smooth'
      });
    }
    leftBtn.addEventListener('click', () => {
      scrollLeft();
      pauseAutoScroll();
    });
    rightBtn.addEventListener('click', () => {
      scrollRight();
      pauseAutoScroll();
    });

    function startAutoScroll() {
      autoScrollInterval = setInterval(() => {
        if (scrollWrapper.scrollLeft + scrollWrapper.clientWidth >= scrollWrapper.scrollWidth) {
          // If reached the end, scroll back to start
          scrollWrapper.scrollTo({
            left: 0,
            behavior: 'smooth'
          });
        } else {
          scrollRight();
        }
      }, 3000);
      autoScrollToggle.textContent = 'Pause Auto-Scroll';
      isAutoScrolling = true;
    }

    function pauseAutoScroll() {
      clearInterval(autoScrollInterval);
      autoScrollToggle.textContent = 'Resume Auto-Scroll';
      isAutoScrolling = false;
    }
    autoScrollToggle.addEventListener('click', () => {
      if (isAutoScrolling) {
        pauseAutoScroll();
      } else {
        startAutoScroll();
      }
    });
    // Start auto-scroll on load
    startAutoScroll();
  });
</script>