<script>
  document.addEventListener("DOMContentLoaded", function() {
    const mobileToggle = document.getElementById("mobileMenuToggle");
    const mobileMenu = document.getElementById("mobileMenu");
    if (mobileToggle && mobileMenu) {
      mobileToggle.addEventListener("click", () => {
        mobileMenu.classList.toggle("d-none");
      });
    }
    // Add focus-visible style
    const style = document.createElement('style');
    style.innerHTML = `
      .nav-link:focus,
      .dropdown-item:focus {
        background-color: rgba(116, 229, 233, 0.49) !important;
        outline: none;
      }
    `;
    document.head.appendChild(style);
    // Keyboard navigation support
    const allDropdownToggles = document.querySelectorAll('.dropdown-toggle');
    allDropdownToggles.forEach(toggle => {
      toggle.setAttribute('aria-haspopup', 'true');
      toggle.setAttribute('aria-expanded', 'false');
      toggle.addEventListener('keydown', function(e) {
        const submenu = toggle.nextElementSibling;
        if (!submenu || !submenu.classList.contains('dropdown-menu')) return;
        switch (e.key) {
          case "Enter":
          case " ":
          case "ArrowDown":
          case "ArrowRight":
            e.preventDefault();
            submenu.classList.add('show');
            toggle.setAttribute('aria-expanded', 'true');
            submenu.querySelector('a')?.focus();
            break;
          case "Escape":
          case "ArrowLeft":
            e.preventDefault();
            submenu.classList.remove('show');
            toggle.setAttribute('aria-expanded', 'false');
            toggle.focus();
            break;
        }
      });
    });
    // Arrow key support within dropdown
    document.querySelectorAll('.dropdown-menu a').forEach(link => {
      link.addEventListener('keydown', function(e) {
        const items = Array.from(link.closest('.dropdown-menu').querySelectorAll('a'));
        const currentIndex = items.indexOf(link);
        switch (e.key) {
          case "ArrowDown":
            e.preventDefault();
            items[(currentIndex + 1) % items.length].focus();
            break;
          case "ArrowUp":
            e.preventDefault();
            items[(currentIndex - 1 + items.length) % items.length].focus();
            break;
          case "ArrowLeft":
            e.preventDefault();
            const parentMenu = link.closest('.dropdown-menu');
            const parentToggle = parentMenu?.parentElement.querySelector('.dropdown-toggle');
            parentMenu?.classList.remove('show');
            parentToggle?.focus();
            break;
          case "Escape":
            e.preventDefault();
            const rootMenu = link.closest('.dropdown-menu');
            rootMenu?.classList.remove('show');
            rootMenu?.parentElement.querySelector('.dropdown-toggle')?.focus();
            break;
        }
      });
    });
    // Submenu toggle for mobile view
    document.querySelectorAll('.submenu-toggle').forEach(toggle => {
      toggle.addEventListener('click', function(e) {
        e.preventDefault();
        const submenu = this.nextElementSibling;
        if (submenu && submenu.tagName === 'UL') {
          submenu.classList.toggle('d-none');
        }
      });
    });
  });
</script>